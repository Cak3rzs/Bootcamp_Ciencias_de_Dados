opcao = -1
 
while opcao != 0:
    opcao = int(input("[1]sacar \n [2]depositar \n [0] sair"))

    if opcao == 1:
        print("Estou contando seu dinheiro, brevemente ele irá sair :D")
    elif opcao == 2:
        print("Olá, voce já pode estar depositando seu dinheiro :D")
    else:
        print("Já vai? Vá pela sombra ao menos ☺")
